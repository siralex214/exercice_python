# 1. Créez deux ensembles, setA avec {1, 2, 3, 4, 5} et setB avec {4, 5, 6, 7, 8}.
setA = {1, 2, 3, 4, 5}
setB = {4, 5, 6, 7, 8}

# 2. Calculez l'union des deux ensembles et affichez le résultat.
union_set = setA | setB
print("Union des ensembles :", union_set)

# 3. Affichez l'intersection des deux ensembles.
intersection_set = setA & setB
print("Intersection des ensembles :", intersection_set)

# 4. Affichez la différence entre setA et setB.
difference_set = setA - setB
print("Différence entre setA et setB :", difference_set)
